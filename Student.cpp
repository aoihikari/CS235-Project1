#include "Student.h"
//---------------------------------------------------------------------------------------
Student::Student(string _id)
{
	id = _id;
	name = "";
	address = "";
	phone = "";
}
//---------------------------------------------------------------------------------------
Student::~Student()
{
}
//---------------------------------------------------------------------------------------
string Student::to_string()
{
	stringstream ss;
	ss << name << endl << id << endl << phone << endl << address << endl;
	
	return ss.str();
}
