#include "Grade.h"
//---------------------------------------------------------------------------------------
Grade::Grade(string _id)
{
	id = _id;
	course = "";
	grade = "";
}
//---------------------------------------------------------------------------------------
Grade::~Grade()
{
}
//---------------------------------------------------------------------------------------
string Grade::to_string()
{
	stringstream ss;
	ss << id <<"\t" << grade << "\t" << course << endl;
	
	return ss.str();
}
